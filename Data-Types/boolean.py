#Boolean data type(used when dealing with True or False Condtions)
print(100 > 99)
print(100 == 99)
print(100 < 99)

#verifying the types of data using python keyword "type"
print(type(100 > 99))
print(type(100 == 99))
print(type(100 < 99))

#print the type of data using python keyword "type"
print(1 == 1, type(1 == 1))
x = 5 > 4
print(x, type(x))

y = 4 > 5
print(y, type(y))
