#Used to check if a value or variable is in a sequence. IN and NOT IN are the membership operators. 
#Results is either True or False depending on the condition

name='Benbella'
profession='DevOps Engineer'
fruits=['banana', 'mango', 'apple']

print('Benbella' in name)
print('Engineer' in profession)
print('banana' in fruits)
