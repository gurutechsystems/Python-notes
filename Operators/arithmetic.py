#Arithmetic operators are used when performing mathematical operations like addition,subtraction,multiplication and division
a=9
b=4

add=a+b #addition
sub=a-b #subtraction
mul=a*b #multiplication
div1=a/b #float division i.e display rational number
div2=a//b #floor division i.e does not display rational numbers

mod=a%b #modulo of both numbers(remainder)
p=a*b #power

#print results
print(add)
print(sub)
print(mul)
print(div1)
print(div2)
print(mod)
print(p)